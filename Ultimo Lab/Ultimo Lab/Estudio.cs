using System;

public class Estudio
{
    string Nombre;
    string Direccion;
    string FechaApertura;

    public Estudio(string Nombre, string Direccion, string FechaApertura)
    {
        this.Nombre = Nombre;
        this.Direccion = Direccion;
        this.FechaApertura = FechaApertura;
    }
}
